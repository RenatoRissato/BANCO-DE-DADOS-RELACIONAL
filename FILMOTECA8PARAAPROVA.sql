---- SELECIONAR TODOS OS FILMES MAIS ANTIGOS DO ALIEN, 
--OU SEJA, ONDE O ANO É MENOR QUE 2000 E TODOS OS FILMES NOVOS DO HIGHLANDER, 
--OU SEJA, ONDE O ANO É POSTERIOR A 2000

SELECT F.NOME_FILME, F.ANO_FILME
FROM FILME F
WHERE ANO_FILME < 2000 AND NOME_FILME LIKE '%ALIEN%' OR NOME_FILME LIKE '%HIGHLANDER%' AND ANO_FILME >2000


--11 - SELECIONAR TODOS OS ANOS DOS FILMES, 
--COM AS QUANTIDADES DE FILMES DE CADA UM, ORDENADO DO ANO MAIS RECENTE PARA O MAIS ANTIGO

SELECT F.ANO_FILME, COUNT(F.NOME_FILME) AS 'QTDE DE FILMES'
FROM FILME F
GROUP BY F.ANO_FILME
ORDER BY F.ANO_FILME DESC

--11,5 - SELECIONAR TODOS OS ANOS DOS FILMES, 
--COM AS QUANTIDADES DE FILMES DE CADA UM, ORDENADO DO ANO MAIS RECENTE PARA O MAIS ANTIGO, 
--POREM RETORNAR APENAS OS ANOS QUE TIVEREM MAIS DE 5 FILMES

SELECT F.ANO_FILME, COUNT(F.NOME_FILME) AS 'QTDE DE FILMES'
FROM FILME F
GROUP BY F.ANO_FILME
HAVING COUNT (F.NOME_FILME) > 5
ORDER BY F.ANO_FILME DESC


--12 - SELECIONAR TODOS OS FILMES DE ANOS COM MAIS DE 3 FILMES

SELECT F.ANO_FILME, COUNT(F.NOME_FILME) AS 'QTDE DE FILMES'
FROM FILME F
GROUP BY F.ANO_FILME
HAVING COUNT (F.NOME_FILME) > 3
ORDER BY F.ANO_FILME DESC


--13 - SELECIONAR TODOS OS DIRETORES QUE FIZERAM MAIS DE UM FILME

SELECT D.NOME_DIRETOR, COUNT(F.NOME_FILME) AS 'QTDE DE FILMES'
FROM DIRETOR D
JOIN FILME F
ON D.ID_DIRETOR = F.ID_DIRETOR
GROUP BY D.NOME_DIRETOR
HAVING COUNT (F.NOME_FILME) >1


--14 - SELECIONAR TODOS OS FILMES EM QUE O PERSONAGEM SINESTRO APARECE 
