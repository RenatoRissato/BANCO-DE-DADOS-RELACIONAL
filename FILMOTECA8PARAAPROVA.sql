LEFT JOIN FILME F
ON F.ID_DIRETOR = D.ID_DIRETOR
WHERE F.NOME_FILME IS NULL


--30 - SELECIONAR OS GENEROS QUE NÃO TEM FILMES ASSOCIADOS

