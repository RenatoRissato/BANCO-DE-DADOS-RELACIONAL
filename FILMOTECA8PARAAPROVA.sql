SELECT F.NOME_FILME, COUNT(A.NOME_ATOR) AS 'QTDE DE ATORES'
FROM ATOR A
JOIN FILME_ATOR FA
ON A.ID_ATOR = FA.ID_ATOR
JOIN FILME F
ON FA.ID_FILME = F.ID_FILME 
GROUP BY F.NOME_FILME
HAVING COUNT (A.NOME_ATOR) <10
