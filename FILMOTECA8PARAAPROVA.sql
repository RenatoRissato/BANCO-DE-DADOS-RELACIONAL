--18 - SELECIONAR OS NOMES DE TODOS OS ATORES QUE TAMBÉM FORAM DIRETORES EM ALGUM FILME

SELECT A.NOME_ATOR, D.NOME_DIRETOR, F.NOME_FILME
FROM ATOR A
JOIN FILME_ATOR FA
ON A.ID_ATOR = FA.ID_ATOR
JOIN FILME F
ON FA.ID_FILME = F.ID_FILME
JOIN DIRETOR D
ON F.ID_DIRETOR = D.ID_DIRETOR
WHERE A.NOME_ATOR = D.NOME_DIRETOR


--19 - SELECIONAR OS NOMES DE TODOS OS ATORES QUE FORAM DIRETORES NO MESMO FILME

SELECT DISTINCT F.NOME_FILME, A.NOME_ATOR, D.NOME_DIRETOR
FROM ATOR A
JOIN FILME_ATOR FA
ON A.ID_ATOR = FA.ID_ATOR
JOIN FILME F
ON FA.ID_FILME = F.ID_FILME
JOIN DIRETOR D
ON F.ID_DIRETOR = D.ID_DIRETOR
WHERE A.NOME_ATOR = D.NOME_DIRETOR

--20 - SELECIONAR OS TRÊS DIRETORES QUE FIZERAM MAIS FILMES

SELECT TOP (3) WITH TIES D.NOME_DIRETOR, COUNT(F.NOME_FILME) AS 'QTDE DE FILMES'
FROM DIRETOR D
JOIN FILME F
ON D.ID_DIRETOR = F.ID_DIRETOR
GROUP BY D.NOME_DIRETOR
ORDER BY [QTDE DE FILMES] DESC

--21 - SELECIONAR OS CINCO GÊNEROS COM MENOS FILMES

SELECT TOP (5) WITH TIES G.NOME_GENERO, COUNT(F.NOME_FILME) AS 'QTDE DE FILMES'
FROM GENERO G
JOIN FILME_GENERO FG
ON G.ID_GENERO = FG.ID_GENERO
JOIN FILME F
ON FG.ID_FILME = F.ID_FILME
GROUP BY G.NOME_GENERO
ORDER BY [QTDE DE FILMES] DESC

--22 - SELECIONAR OS GÊNEROS DE TODOS OS FILMES DO ANO QUE TEM A MAIOR QUANTIDADE DE FILMES

SELECT G.NOME_GENERO, F.ANO_FILME, COUNT(*) AS 'QTDE DE FILMES'
FROM GENERO G
JOIN FILME_GENERO FG
ON G.ID_GENERO = FG.ID_GENERO 
JOIN FILME F
ON FG.ID_FILME = F.ID_FILME
WHERE ANO_FILME = 2011
GROUP BY G.NOME_GENERO, F.ANO_FILME
ORDER BY [QTDE DE FILMES] DESC


--23 - SELECIONAR TODOS OS ATORES QUE TENHAM O MESMO NOME DO PERSONAGEM QUE ENCENARAM NOS FILMES

SELECT DISTINCT F.NOME_FILME, A.NOME_ATOR, FA.NOME_PERSONAGEM
FROM ATOR A
JOIN FILME_ATOR FA
ON A.ID_ATOR = FA.ID_ATOR
JOIN FILME F
ON FA.ID_FILME = F.ID_FILME
WHERE A.NOME_ATOR = FA.NOME_PERSONAGEM


--24 - SELECIONAR TODOS OS ATORES QUE TENHAM FEITO MAIS DE UM PERSONAGEM NO MESMO FILME

SELECT F.NOME_FILME, A.NOME_ATOR, COUNT(FA.NOME_PERSONAGEM) AS 'QTDE DE PERSONAGENS'
FROM ATOR A
JOIN FILME_ATOR FA
ON A.ID_ATOR = FA.ID_ATOR
JOIN FILME F
ON FA.ID_FILME = F.ID_FILME
GROUP BY F.NOME_FILME, A.NOME_ATOR
HAVING COUNT (FA.NOME_PERSONAGEM) >1
ORDER BY [QTDE DE PERSONAGENS] DESC


--25 - SELECIONAR OS NOMES ORIGINAIS DE TODOS OS FILMES QUE TENHAM MENOS DE 10 ATORES

SELECT NOME_ORIGINAL_FILME, COUNT(A.NOME_ATOR) AS 'QTDE DE ATORES'
FROM FILME F
JOIN FILME_ATOR FA
ON F.ID_FILME = FA.ID_ATOR
JOIN ATOR A
ON FA.ID_ATOR = A.ID_ATOR
GROUP BY NOME_ORIGINAL_FILME
HAVING COUNT (A.NOME_ATOR) <10
ORDER BY [QTDE DE ATORES] DESC


