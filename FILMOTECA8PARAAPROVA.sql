ON FG.ID_FILME = F.ID_FILME
JOIN GENERO G
ON FG.ID_GENERO = G.ID_GENERO

--10 - SELECIONAR TODOS OS GÊNEROS COM AS QUANTIDADES DE FILMES DE CADA UM

